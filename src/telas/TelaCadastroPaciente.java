    private void jButtonSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvar1ActionPerformed
        this.pac.setNome(jTextFieldNome.getText().toUpperCase());
        this.pac.setTelefone1(jTextFieldTelefone.getText());
        this.pac.setTelefone2(jTextFieldCelular.getText());
        this.pac.setEndereco(jTextFieldEndereco.getText().toUpperCase());
        this.pac.setEmail(jTextFieldEmail.getText().toLowerCase());
        this.pac.setDataNasc(jDateChooserDataNascimento.getDate());
        this.pac.setIdconvenio((Convenio) jComboBoxConvenio.getSelectedItem());
        this.pac.setObservacoes(jTextAreaObservacoes.getText());
        this.pac.setCpf(jTextFieldCPF.getText());
        try {
            boolean gravou = pDAO.inserir(this.pac);
            if (gravou) {
                this.dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonSalvar1ActionPerformed

